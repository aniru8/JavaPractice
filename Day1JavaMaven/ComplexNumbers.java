package com.example.Day1JavaMaven;

public class ComplexNumbers {
	
	int real,imag;

	public ComplexNumbers(int real, int imag) {
		super();
		this.real = real;
		this.imag = imag;
	}

	@Override
	public String toString() {
		return "ComplexNumbers [real=" + real + ", imag=" + imag + "]";
	}
	
	

}
